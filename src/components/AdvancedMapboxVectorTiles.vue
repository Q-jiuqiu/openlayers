<template>
  <div class="MapView">
    <div id="map" class="map"></div>
    <!-- 
      Vector tiles就是将矢量数据直接进行发布，
    也是按照切片对应的级别进行请求，返回的数据包括地图的基本信息，还有样式的信息，
    在客户端进行渲染，达到同样的地图效果，这样在数据变化时，只需在服务端将矢量数据进行更新即可。 -->
  </div>
</template>

<script>
import "ol/ol.css";
import MVT from "ol/format/MVT"; //用于以Mapbox MVT格式读取数据的功能格式
import Map from "ol/Map";
import TileGrid from "ol/tilegrid/TileGrid"; //用于为访问平铺图像服务器的源设置网格模式的基类
import VectorTileLayer from "ol/layer/VectorTile"; // 呈现给客户端的矢量切片数据的图层
import VectorTileSource from "ol/source/VectorTile"; //提供将矢量数据划分为图块网格的功能
import View from "ol/View";
import { Fill, Icon, Stroke, Style, Text } from "ol/style";
import { get as getProjection } from "ol/proj"; //为指定的代码获取一个Projection对象

export default {
  data() {
    return {
      map: null,
      key: ""
    };
  },
  mounted() {
    this.initMap();
  },
  methods: {
    initMap() {
      this.key =
        "pk.eyJ1IjoicXUtbCIsImEiOiJja2xqMWl4ZHg1cmFqMnZ1aWhvZjN4c2dvIn0.5BlSYZFk4p41s6I7HeXhxA";

      // Calculation of resolutions that match zoom levels 1, 3, 5, 7, 9, 11, 13, 15.
      var resolutions = [];
      for (var i = 0; i <= 8; ++i) {
        resolutions.push(156543.03392804097 / Math.pow(2, i * 2));
      }
      console.log("resolutions", resolutions);
      // Calculation of tile urls for zoom levels 1, 3, 5, 7, 9, 11, 13, 15.
      this.map = new Map({
        layers: [
          new VectorTileLayer({
            source: new VectorTileSource({
              // attributions:在地图上右下角显示版权信息
              // attributions:
              //   '© <a href="https://www.mapbox.com/map-feedback/">Mapbox</a> ' +
              //   '© <a href="https://www.openstreetmap.org/copyright">' +
              //   "OpenStreetMap contributors</a>",
              // format:定义格式
              format: new MVT(),
              tileGrid: new TileGrid({
                //extent:瓦片网格的范围,源将不请求超出此范围的图块
                extent: getProjection("EPSG:3857").getExtent(), //返回extent图层的
                //resolutions: 每个分辨率的数组索引都需要匹配缩放级别
                resolutions: resolutions,
                // tileSize:瓷砖尺寸
                tileSize: 1024
              }),
              tileUrlFunction: this.tileUrlFunction //可选功能，用于在给定URL的情况下加载图块
            }),
            style: this.createMapboxStreetsV6Style(
              Style,
              Fill,
              Stroke,
              Icon,
              Text
            )
          })
        ],
        target: "map",
        view: new View({
          center: [0, 0], // 宜宾
          // minZoom: 1,
          zoom: 2
        })
      });
    },
    tileUrlFunction(tileCoord) {
      let res = (
        "https://{a-d}.tiles.mapbox.com/v4/mapbox.mapbox-streets-v6/" +
        "{z}/{x}/{y}.vector.pbf?access_token=" +
        this.key
      )
        .replace("{z}", String(tileCoord[0] * 2 - 1))
        .replace("{x}", String(tileCoord[1]))
        .replace("{y}", String(tileCoord[2]))
        .replace(
          "{a-d}",
          "abcd".substr(((tileCoord[1] << tileCoord[0]) + tileCoord[2]) % 4, 1)
        );
      return res;
    },
    createMapboxStreetsV6Style(Style, Fill, Stroke, Icon, Text) {
      var fill = new Fill({ color: "" });
      var stroke = new Stroke({ color: "", width: 1 });
      var polygon = new Style({ fill: fill });
      var strokedPolygon = new Style({ fill: fill, stroke: stroke });
      var line = new Style({ stroke: stroke });
      var text = new Style({
        text: new Text({
          text: "",
          fill: fill,
          stroke: stroke
        })
      });
      var iconCache = {};
      function getIcon(iconName) {
        var icon = iconCache[iconName];
        if (!icon) {
          icon = new Style({
            image: new Icon({
              src:
                "https://cdn.rawgit.com/mapbox/maki/master/icons/" +
                iconName +
                "-15.svg",
              imgSize: [15, 15]
            })
          });
          iconCache[iconName] = icon;
        }
        return icon;
      }
      var styles = [];
      return function(feature, resolution) {
        var length = 0;
        var layer = feature.get("layer");
        var cls = feature.get("class");
        var type = feature.get("type");
        var scalerank = feature.get("scalerank");
        var labelrank = feature.get("labelrank");
        var adminLevel = feature.get("admin_level");
        var maritime = feature.get("maritime");
        var disputed = feature.get("disputed");
        var maki = feature.get("maki");
        var geom = feature.getGeometry().getType();
        if (layer == "landuse" && cls == "park") {
          fill.setColor("#d8e8c8");
          styles[length++] = polygon;
        } else if (layer == "landuse" && cls == "cemetery") {
          fill.setColor("#e0e4dd");
          styles[length++] = polygon;
        } else if (layer == "landuse" && cls == "hospital") {
          fill.setColor("#fde");
          styles[length++] = polygon;
        } else if (layer == "landuse" && cls == "school") {
          fill.setColor("#f0e8f8");
          styles[length++] = polygon;
        } else if (layer == "landuse" && cls == "wood") {
          fill.setColor("rgb(233,238,223)");
          styles[length++] = polygon;
        } else if (
          layer == "waterway" &&
          cls != "river" &&
          cls != "stream" &&
          cls != "canal"
        ) {
          stroke.setColor("#a0c8f0");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "waterway" && cls == "river") {
          stroke.setColor("#a0c8f0");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "waterway" && (cls == "stream" || cls == "canal")) {
          stroke.setColor("#a0c8f0");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "water") {
          fill.setColor("#a0c8f0");
          styles[length++] = polygon;
        } else if (layer == "aeroway" && geom == "Polygon") {
          fill.setColor("rgb(242,239,235)");
          styles[length++] = polygon;
        } else if (
          layer == "aeroway" &&
          geom == "LineString" &&
          resolution <= 76.43702828517625
        ) {
          stroke.setColor("#f0ede9");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "building") {
          fill.setColor("#f2eae2");
          stroke.setColor("#dfdbd7");
          stroke.setWidth(1);
          styles[length++] = strokedPolygon;
        } else if (layer == "tunnel" && cls == "motorway_link") {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "tunnel" && cls == "service") {
          stroke.setColor("#cfcdca");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "tunnel" &&
          (cls == "street" || cls == "street_limited")
        ) {
          stroke.setColor("#cfcdca");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "tunnel" &&
          cls == "main" &&
          resolution <= 1222.99245256282
        ) {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "tunnel" && cls == "motorway") {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "tunnel" && cls == "path") {
          stroke.setColor("#cba");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "tunnel" && cls == "major_rail") {
          stroke.setColor("#bbb");
          stroke.setWidth(2);
          styles[length++] = line;
        } else if (layer == "road" && cls == "motorway_link") {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "road" &&
          (cls == "street" || cls == "street_limited") &&
          geom == "LineString"
        ) {
          stroke.setColor("#cfcdca");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "road" &&
          cls == "main" &&
          resolution <= 1222.99245256282
        ) {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "road" &&
          cls == "motorway" &&
          resolution <= 4891.96981025128
        ) {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "road" && cls == "path") {
          stroke.setColor("#cba");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "road" && cls == "major_rail") {
          stroke.setColor("#bbb");
          stroke.setWidth(2);
          styles[length++] = line;
        } else if (layer == "bridge" && cls == "motorway_link") {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "bridge" && cls == "motorway") {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "bridge" && cls == "service") {
          stroke.setColor("#cfcdca");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "bridge" &&
          (cls == "street" || cls == "street_limited")
        ) {
          stroke.setColor("#cfcdca");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "bridge" &&
          cls == "main" &&
          resolution <= 1222.99245256282
        ) {
          stroke.setColor("#e9ac77");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "bridge" && cls == "path") {
          stroke.setColor("#cba");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "bridge" && cls == "major_rail") {
          stroke.setColor("#bbb");
          stroke.setWidth(2);
          styles[length++] = line;
        } else if (layer == "admin" && adminLevel >= 3 && maritime === 0) {
          stroke.setColor("#9e9cab");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "admin" &&
          adminLevel == 2 &&
          disputed === 0 &&
          maritime === 0
        ) {
          stroke.setColor("#9e9cab");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (
          layer == "admin" &&
          adminLevel == 2 &&
          disputed === 1 &&
          maritime === 0
        ) {
          stroke.setColor("#9e9cab");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "admin" && adminLevel >= 3 && maritime === 1) {
          stroke.setColor("#a0c8f0");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "admin" && adminLevel == 2 && maritime === 1) {
          stroke.setColor("#a0c8f0");
          stroke.setWidth(1);
          styles[length++] = line;
        } else if (layer == "country_label" && scalerank === 1) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('bold 11px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#334");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(2);
          styles[length++] = text;
        } else if (
          layer == "country_label" &&
          scalerank === 2 &&
          resolution <= 19567.87924100512
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('bold 10px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#334");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(2);
          styles[length++] = text;
        } else if (
          layer == "country_label" &&
          scalerank === 3 &&
          resolution <= 9783.93962050256
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('bold 9px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#334");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(2);
          styles[length++] = text;
        } else if (
          layer == "country_label" &&
          scalerank === 4 &&
          resolution <= 4891.96981025128
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('bold 8px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#334");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(2);
          styles[length++] = text;
        } else if (
          layer == "marine_label" &&
          labelrank === 1 &&
          geom == "Point"
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('italic 11px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#74aee9");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "marine_label" &&
          labelrank === 2 &&
          geom == "Point"
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('italic 11px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#74aee9");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "marine_label" &&
          labelrank === 3 &&
          geom == "Point"
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('italic 10px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#74aee9");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "marine_label" &&
          labelrank === 4 &&
          geom == "Point"
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('italic 9px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#74aee9");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "place_label" &&
          type == "city" &&
          resolution <= 1222.99245256282
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('11px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#333");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "place_label" &&
          type == "town" &&
          resolution <= 305.748113140705
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('9px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#333");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "place_label" &&
          type == "village" &&
          resolution <= 38.21851414258813
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('8px "Open Sans", "Arial Unicode MS"');
          fill.setColor("#333");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "place_label" &&
          resolution <= 19.109257071294063 &&
          (type == "hamlet" || type == "suburb" || type == "neighbourhood")
        ) {
          text.getText().setText(feature.get("name_en"));
          text.getText().setFont('bold 9px "Arial Narrow"');
          fill.setColor("#633");
          stroke.setColor("rgba(255,255,255,0.8)");
          stroke.setWidth(1);
          styles[length++] = text;
        } else if (
          layer == "poi_label" &&
          resolution <= 19.109257071294063 &&
          scalerank == 1 &&
          maki !== "marker"
        ) {
          styles[length++] = getIcon(maki);
        } else if (
          layer == "poi_label" &&
          resolution <= 9.554628535647032 &&
          scalerank == 2 &&
          maki !== "marker"
        ) {
          styles[length++] = getIcon(maki);
        } else if (
          layer == "poi_label" &&
          resolution <= 4.777314267823516 &&
          scalerank == 3 &&
          maki !== "marker"
        ) {
          styles[length++] = getIcon(maki);
        } else if (
          layer == "poi_label" &&
          resolution <= 2.388657133911758 &&
          scalerank == 4 &&
          maki !== "marker"
        ) {
          styles[length++] = getIcon(maki);
        } else if (
          layer == "poi_label" &&
          resolution <= 1.194328566955879 &&
          scalerank >= 5 &&
          maki !== "marker"
        ) {
          styles[length++] = getIcon(maki);
        }
        styles.length = length;
        return styles;
      };
    }
  }
};
